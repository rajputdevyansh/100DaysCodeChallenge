# A program to create a circle
import turtle

screen = turtle.Screen()
screen.bgcolor("black")
screen.title("Drawing")
my_turtle = turtle.Turtle()
my_turtle.color("red")
my_turtle.pensize(15)
my_turtle.fillcolor("white")
my_turtle.begin_fill()
my_turtle.circle(100)
my_turtle.end_fill()
turtle.done()
